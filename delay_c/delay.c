#include "delay.h"


void delay1ms()		   //@12MHz
{
	uint i,j;
    for(j=1;j>0;j--)
    for(i=112;i>0;i--);
}

/*********************************************************************************************
函数名：12MHZ晶振延时s ms
调  用：delay_ms（s）
参  数：s  // 延时s ms
返回值：无
**********************************************************************************************/
void delay_ms(usint s)
{
	while(s--)
	{
		delay1ms();
	}
}
